var fs = require('fs');
var multer=require('multer');


function uploadImgIn(dir){
    let diskStr={
      destination: "./public/".concat(dir),
      filename: function(req,res,cb){
        cb(null, "IMG_" + Date.now() + ".jpg");
      }
    };
    return multer({
      storage: multer.diskStorage(diskStr)
    });
}



